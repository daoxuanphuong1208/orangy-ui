const productGridTabsBottom = document.querySelector(
  ".product-grid-tabs__bottom"
);
const productGridTabsTopList = document.querySelectorAll(
  ".product-grid-tabs__top-list-item"
);

function renderTabContent(tabName) {
  const tabData = productGridTabsList.find((item) => item.name === tabName);
  if (!tabData) return;

  let productGridRender = `
  <div data-aos="zoom-in">
  <ul class="product-grid-tabs__bottom-list ${tabName}">`;
  productGridRender += tabData.data
    .map(
      (product, index) => `
        <li class="product-grid-tabs__bottom-list-item item${index + 1}">
          <div class="product-grid-tabs__bottom-list-item-wrap-img">
            <img src="${product.img}" alt="${product.name}" />
          </div>
          <ul class="product-grid-tabs__bottom-list-item-text">
            <span>${product.type}</span>
            <span>${product.name}</span>
            ${
              product.priceNew
                ? `<span>
                    <span>${product.priceOld}</span>
                    <span>${product.priceNew}</span>
                  </span>`
                : `<span>${product.priceOld}</span>`
            }
          </ul>
          ${
            product.sale === SALES.FAST
              ? `<div class="sales-container">
                  <div class="sales-fast">
                    <div>Selling fast!</div>
                    <div>New</div>
                  </div>
                </div>`
              : ""
          }
           ${
             product.sale === SALES.NORMAL
               ? `<div class="sales-container">
                  <div class="sales-normal">-30%</div>
                </div>`
               : ""
           }
        </li>`
    )
    .join("");
  productGridRender += `</div>`;

  productGridTabsBottom.innerHTML = productGridRender;
}

const defaultTab = document.querySelector(
  ".product-grid-tabs__top-list-item.active"
)?.dataset.tab;
if (defaultTab) {
  renderTabContent(defaultTab);
}

productGridTabsTopList.forEach((tab) => {
  tab.addEventListener("click", function () {
    productGridTabsTopList.forEach((tabItem) =>
      tabItem.classList.remove("active")
    );
    this.classList.add("active");
    renderTabContent(this.dataset.tab);
  });
});
